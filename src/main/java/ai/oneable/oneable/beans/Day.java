package ai.oneable.oneable.beans;

public class Day {
    private String day;
    private long totalDayDuration;

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public long getTotalDayDuration() {
        return totalDayDuration;
    }

    public void setTotalDayDuration(long totalDayDuration) {
        this.totalDayDuration = totalDayDuration;
    }
}